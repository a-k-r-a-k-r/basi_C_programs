//difference b/w integer,float,etc....
#include<stdio.h>
void main(){
    int a=-20;
    float b=-20;
    double c=-20;
    char d='a';
    long unsigned e=-20;
    unsigned f=-20;
    printf("integer=%d\n",a);
    printf("float=%f\n",b);
    printf("character=%c\n",d);
    printf("double=%lf\n",c);
    printf("long unsigned=%lu\n",e);
    printf("unsigned=%u\n",f);
}
